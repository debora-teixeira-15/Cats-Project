@Composable
fun CatsListScreen(navController: NavController,
                   viewModel: CatsListViewModel = koinViewModel()) {
    var search by remember { mutableStateOf("") }

    val catsList = remember { viewModel.catsList }

    val filteredCatsList = catsList.value.filter { catListEntry -> catListEntry.breed.lowercase().contains(search.lowercase()) }


    Column(modifier = Modifier.fillMaxSize()) {
        SearchBar(search = search, onValueChange = { search = it })
        FavoriteFilterButton()
        LazyVerticalGrid(
            columns = GridCells.Adaptive(minSize = 120.dp),
            contentPadding = PaddingValues(12.dp),
            verticalArrangement = Arrangement.spacedBy(25.dp),
            horizontalArrangement = Arrangement.spacedBy(10.dp),
        ) {
            items(filteredCatsList) { cat ->
                CatCard(cat.breed, navController)
            }
        }
    }
}

@Composable
private fun FavoriteFilterButton(
) {
    var isFavoritesOnly by remember { mutableStateOf(false) }
    Row {
        IconButton(
            onClick = { isFavoritesOnly = !isFavoritesOnly },
            modifier = Modifier.size(30.dp)
        ) {
            Icon(
                imageVector = if (isFavoritesOnly) Icons.Default.Favorite else Icons.Default.FavoriteBorder,
                contentDescription = if (isFavoritesOnly) "Show All" else "Show Favorites Only",
                tint = if (isFavoritesOnly) Color.Red else Color.Gray
            )


        }
        Text(
            text = if (isFavoritesOnly) "Show All" else "Show Favorites Only",
            modifier = Modifier.offset(x = 2.dp, y = 4.dp),
            fontSize = 11.sp
        )
    }
}